package it.polimi.se2018.utils;

/**
 * The class {@code NoSuchParamInMessageException} is a form of {@link Exception}
 * that is thrown if a given message has not the parameter that is asked to retrieve.
 *
 * @author Federico Haag
 */
public class NoSuchParamInMessageException extends Exception {
    /**
     * Basic constructor. Do nothing else than calling super()
     */
    public NoSuchParamInMessageException() {
        super();
        //do nothing else
    }
}
